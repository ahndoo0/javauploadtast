package test;

public class tw01_01 {
    public static void main(String[] args){
        
        System.out.println("Hello world");//Hello world를 출력하시오
        
        System.out.println("Hello");//Hello 를 출력하시오
        System.out.println("world");//world 를 출력하시오
        
        System.out.println("Hello, \nworld");//Hello(줄바꿈) world
        System.out.println("Hello, \tworld");//Hello (탭 특수문자)world
    }
    
}
